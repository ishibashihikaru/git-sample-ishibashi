package sample;


public class Car {
	
	/** スピード */
	private int speed;
	/** ガソリン */
	private int gas;
	
	private String tire;
	
	
	public String getTire() {
		return tire;
	}
	public void setTire(String tire) {
		this.tire = tire;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public int getGas() {
		return gas;
	}
	public void setGas(int gas) {
		this.gas = gas;
	}
	
	

}
